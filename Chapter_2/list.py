# list.py

# 3중 리스트 인덱싱
a = [1, 2, ['a', 'b', ['Life', 'is']]]
print(a[2][2][0])

# 리스트 슬라이싱
a = [1, 2, 3, 4, 5]
b = a[:2]
c = a[2:]
print(b)
print(c)

# 중첩된 리스트에서 슬라이싱
a = [1, 2, 3, ['a', 'b', 'c'], 4, 5]
print(a[2:5])
print(a[3][:2])

